    public static function byYear($year)
    {
        $values = self::all()[$year];

        foreach ($values as $key => $value) {
            $values[$key]['year'] = $year;
            $values[$key]['jpg'] =
                "/files/apps/parlamentojuvenil/clipping/$year/" .
                $value['file'] .
                '.jpg';
            $values[$key]['pdf'] =
                "/files/apps/parlamentojuvenil/clipping/$year/" .
                $value['file'] .
                '.pdf';
        }

        return $values;
    }
